# Operadores Aritmeticos
# Suma (+): Se utiliza para sumar dos valores.
# Resta (-): Se utiliza para restar un valor de otro.
# Multiplicación (*): Se utiliza para multiplicar dos valores.
# División (/): Se utiliza para dividir un valor por otro. Siempre devuelve un resultado en formato de número decimal (float).
# División Entera (//): Devuelve la parte entera del resultado de la división.
# Módulo (%): Devuelve el resto de la división entera de dos valores.
# Potencia ( o pow()):** Calcula la potencia de un número elevado a otro.

a = 10
b = 3

suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
division_entera = a // b
modulo = a % b 
potencia = a ** b

print("Suma: " , suma)
print("Resta: ", resta)
print("Multiplicación: ", multiplicacion)
print("División: ", division)
print("División Entera: ", division_entera)
print("Modulo: ", modulo)
print("Potencia: ", potencia)

